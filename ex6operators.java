public class ex6operators {
    public static void main(String[] args) {
        // constants
        // after this final keyword we cannot change the value of the variable
        final float PI = 3.14F;
        System.out.println(PI);

        // operators
        // arithemetic operators
        // sum +
        // diff -
        // multiply *
        // divide /
        // modulo % (gives the remainder as the output)

        double a = 7;
        double b = 5;
        double modulo = a % b;
        System.out.println(modulo);
        // assignment operator

        int no = 2;
        // no = no + 2;
        // no++;
        // to add or minus 1 befor or after
        System.out.println(no++);
        System.out.println(no);

    }
}
